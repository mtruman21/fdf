#include "libft.h"

void	ft_strarraydel(void ***str)
{
	int x;

	x = 0;
	while ((*str)[x] != NULL)
	{
		ft_strdel(&(*str)[x]);
		x++;
	}
	ft_strdel((void **)str);
}
